def start_game(width=132, delay=0.1, message=">>> PRESS 'ENTER' TO START GAME <<<"):
    init(autoreset=True)

    for index in range(1, len(message) + 1):
        print(Fore.LIGHTBLUE_EX + f'{message[:index]:^{width}}', end="\r", flush=True)
        sleep(delay)

    keyboard.wait('enter')
